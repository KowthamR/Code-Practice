# 10.4 Wood Finder

text = """How much wood would a woodchuck chuck
        If a woodchuck could chuck wood?
        He would chuck , he would , as much as he could ,
        And chuck as much as a woodchuck would
        If a woodchuck could chuck wood."""
text=text.lower()
w='wood'
count_wood=text.count(w)
print(count_wood)
